import Foundation
import UIKit

extension UIColor {
    class func primaryRedColor() -> UIColor {
        return UIColor(red: 1.0, green: 0.0667, blue: 0.1765, alpha: 1.0)
    }
    
    class func primaryGreenColor() -> UIColor {
        return UIColor(red: 0.0, green: 0.3922, blue: 0.0, alpha: 1.0)
    }
}
